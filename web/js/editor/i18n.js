// ── Language detection ──────────────────────────────────────────────────────

function detectLang() {
  const stored = localStorage.getItem("me-lang");
  if (stored && MESSAGES[stored]) return stored;
  const nav = (navigator.language || "en").slice(0, 2).toLowerCase();
  if (nav === "ja") return "ja";
  if (nav === "zh") return "zh";
  return "en";
}

let _lang = detectLang();

export function getLang() { return _lang; }

export function setLang(lang) {
  if (MESSAGES[lang]) {
    _lang = lang;
    localStorage.setItem("me-lang", lang);
  }
}

/**
 * Translate key, with optional {placeholder} substitution.
 * Falls back to English, then to the bare key.
 */
export function t(key, params) {
  const msgs = MESSAGES[_lang] || MESSAGES.en;
  let str = Object.prototype.hasOwnProperty.call(msgs, key)
    ? msgs[key]
    : (MESSAGES.en[key] ?? key);
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replaceAll(`{${k}}`, String(v));
    }
  }
  return str;
}
